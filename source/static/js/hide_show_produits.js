$(document).ready(function(){
    $(".table_1").show();
    $(".table_2").hide();
    $(".table_3").hide();
    $(".item_1_1").click(function(){
        $(".table_1").show();
        $(".table_2").hide();
        $(".table_3").hide();
    });
    $(".item_1_2").click(function(){
        $(".table_1").hide();
        $(".table_2").show();
        $(".table_3").hide();
    });
    $(".item_1_3").click(function(){
        $(".table_1").hide();
        $(".table_2").hide();
        $(".table_3").show();
    });
});